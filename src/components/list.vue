<template>
    <div class="list" v-show="list.length">
        <div class="list-control">
            <div class="list-control-filter">
                <span>品牌:</span>
                <span
                 class="list-control-filter-item"
                 v-for="item in brands" :key="item"
                 :class="{on: item === filterBrand}"
                 @click="handleFilterBrand(item)"> {{item}} </span>
            </div>
            <div class="list-control-filter">
                <span>颜色：</span>
                <span
                 class="list-control-filter-item"
                 :class="{on: item === filterColor}"
                 v-for="item in colors" :key="item.id"
                 @click="handleFilterColor(item)"> {{item}} </span>
            </div>
            <div class="list-control-order">
                <span>排序：</span>
                <span 
                    class="list-control-order-item"
                     :class="{on: order === ''}"
                      @click="handleOrderDefault">默认</span>
                <span 
                    class="list-control-order-item"
                    :class="{on: order === 'sales'}" 
                    @click="handleOrderSales">销量
                        <template v-if="order === 'sales'">↓</template>
                </span>
                <span 
                    class="list-control-order-item"
                    :class="{on: order.indexOf('cost') > -1}" 
                    @click="handleOrderCost">价格
                        <template v-if="order === 'cost-asc'">↑</template>
                        <template v-if="order === 'cost-desc'">↓</template>
                </span>
                
            </div>
        </div>
        <app-product v-for="item of filteredAndOrderedList" :info="item" :key="item.id"></app-product>
        <div class="product-not-found" v-show="!filteredAndOrderedList.length"> 暂无相关商品 </div>
    </div>
</template>

<script>
    import Product from './product.vue';
    export default {
        components:{
            'app-product':Product
        },
        data() {
            return{
                //排序依据
                // sales, //(销量)
                // cost-desc, //(价格降序)
                // cost-asc, //(价格升序)
                order: '',
                filterBrand: '',
                filterColor: ''         
            }
        },
        computed: {
            list(){
                //获取Vuex列表数据
                return this.$store.state.productList;
            },
            brands(){
                return this.$store.getters.brands;
            },
            colors(){
                return this.$store.getters.colors;
            },
            filteredAndOrderedList(){
                //复制原数据
                let list = [...this.list];
                //按品牌过滤
                if(this.filterBrand !== ''){
                    list = list.filter(item => item.brand === this.filterBrand);
                }
                //按颜色过滤
                if(this.filterColor !== ''){
                    list = list.filter(item => item.color === this.filterColor);
                }
                //排序
                if(this.order !== ''){
                    if(this.order === 'sales'){
                        list = list.sort((a, b) => b.sales - a.sales)
                    }else if (this.order === 'cnst-desc'){
                        list = list.sort((a, b) => b.cost - a.cost)
                    }else if(this.order === 'cost-asc'){
                        list = list.sort((a, b) => a.cost - b.cost)
                    }
                }
                console.log(list);
                return list;
            },
            
        },
        mounted(){
            //初始化时，通过Vuex的action请求数据
            this.$store.dispatch('getProductList');
        },
        methods:{
            handleOrderDefault(){
                this.order = ''
            },
            handleOrderSales(){
                
                this.order = 'sales'
            },
            handleOrderCost(){
                if(this.order === 'cost-desc'){
                    this.order = 'cost-asc'
                }else{
                    this.order = 'cost-desc'
                }
            },
            handleFilterBrand(brand){
                
                //单次点击选中，再次点击取消
                if(this.filterBrand === brand){
                    this.filterBrand = '';
                }else{
                    this.filterBrand = brand;
                }
                console.log(this.filterBrand);
            },
            handleFilterColor(color){
                console.log(111);
                //单次点击选中，再次点击取消
                if(this.filterColor === color){
                    this.filterColor = '';
                }else{
                    this.filterColor = color;
                }
            }
        }
    }
</script>

